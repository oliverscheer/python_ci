""" some tools """

class CalcTools:
    """ some string tools """

    def __init__(self, val):
        print("val", val)
        self.val = val

    def add(self, a, b):
        """ returns upper string """
        return a + b

    def sub(self, a, b):
        """ returns lower string """
        return a - b

    def multi(self, a, b):
        return a * b

    def div(self, a, b):
        return a / b
